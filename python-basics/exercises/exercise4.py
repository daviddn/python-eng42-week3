
# Dictionary basics :D
# 1 - Define a dictionary call story1, it should have the followign keys:
# Start, Middle and End

story1 = {
    'Start': 'Once upon a time',
    'Middle': 'there was a llama',
    'End': 'and then he died'
}

# 2 - Print the entire dictionary

print(story1)

# 3 - Print the type of your dictionary

print(type(story1))

# 4 - Print only the keys

print(story1.keys())

# 5 - print only the values

print(story1.values())

# 6 - print the first each individual value using the keys

print(story1['Start'], story1['Middle'], story1['End'])
